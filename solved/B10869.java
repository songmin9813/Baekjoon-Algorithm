import java.util.Scanner;
public class B10869 {
	public static void main(String[] args) {
		Scanner input = new Scanner(System.in);
		int x;
		int y;
		x=input.nextInt();
		y=input.nextInt();
		System.out.println(x+y);
		System.out.println(x-y);
		System.out.println(x*y);
		System.out.println(x/y);
		System.out.println(x%y);
		input.close();
	}
}
